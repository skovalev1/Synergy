# Вводим числа A и B
A = int(input("Введите число A: "))
B = int(input("Введите число B: "))

# Проверяем, что A не превышает B
if A > B:
    print("Ошибка: A должно быть меньше или равно B")
else:
    # Выводим все четные числа на заданном отрезке
    for num in range(A, B + 1):
        if num % 2 == 0:
            print(num, end=' ')
