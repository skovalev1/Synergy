# Вводим число X
X = int(input("Введите натуральное число X: "))

# Проверяем, что X удовлетворяет условию задачи
if not 1 <= X <= 2*10**9:
    print("Ошибка: X должно быть в диапазоне от 1 до 2*10^9")
else:
    # Подсчитываем количество делителей
    count = 0
    i = 1
    while i * i <= X:
        if X % i == 0:
            count += 2 if X // i != i else 1
        i += 1

    print(f"Количество натуральных делителей числа X: {count}")
