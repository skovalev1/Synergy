# Вводим число N
N = int(input())

# Проверяем, что N удовлетворяет условию задачи
if not 1 <= N <= 100000:
    print("Ошибка: N должно быть в диапазоне от 1 до 100000")
else:
    # Вводим N чисел и преобразуем их в список
    numbers = list(map(int, input().split()))

    # Проверяем, что каждое число удовлетворяет условию задачи
    if any(abs(number) > 2*10**9 for number in numbers):
        print("Ошибка: каждое число должно быть по модулю не больше 2*10^9")
    else:
        # Создаем множество из списка чисел, чтобы удалить дубликаты
        unique_numbers = set(numbers)

        # Выводим количество уникальных чисел
        print(len(unique_numbers))
