# Вводим количество чисел в первом списке
N1 = int(input())

# Проверяем, что N1 удовлетворяет условию задачи
if not 1 <= N1 <= 100000:
    print("Ошибка: количество чисел в первом списке должно быть от 1 до 100000")
else:
    # Вводим первый список чисел
    list1 = set(int(input()) for _ in range(N1))

    # Вводим количество чисел во втором списке
    N2 = int(input())

    # Проверяем, что N2 удовлетворяет условию задачи
    if not 1 <= N2 <= 100000:
        print("Ошибка: количество чисел во втором списке должно быть от 1 до 100000")
    else:
        # Вводим второй список чисел
        list2 = set(int(input()) for _ in range(N2))

        # Находим пересечение двух списков
        common_numbers = list1 & list2

        # Выводим количество общих чисел
        print(len(common_numbers))
