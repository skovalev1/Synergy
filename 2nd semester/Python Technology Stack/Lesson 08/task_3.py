# Вводим число m и n
m = int(input())
n = int(input())

# Проверяем, что m и n удовлетворяют условиям задачи
if not 1 <= m <= 10**6:
    print("Ошибка: m должно быть в диапазоне от 1 до 10^6")
elif not 1 <= n <= 100:
    print("Ошибка: n должно быть в диапазоне от 1 до 100")
else:
    # Вводим вес каждого рыбака и добавляем его в список
    weights = []
    for _ in range(n):
        weight = int(input())
        # Проверяем, что вес удовлетворяет условию задачи
        if not 1 <= weight <= m:
            print("Ошибка: вес каждого рыбака должен быть в диапазоне от 1 до m")
            break
        weights.append(weight)
    else:
        weights.sort()

        # Инициализируем счетчик лодок
        boats = 0

        # Инициализируем указатели на начало и конец списка
        start = 0
        end = n - 1

        # Пока начальный указатель меньше или равен конечному
        while start <= end:
            # Если вес двух рыбаков не превышает максимальный вес лодки
            if weights[start] + weights[end] <= m:
                # Увеличиваем начальный указатель
                start += 1
            # Уменьшаем конечный указатель
            end -= 1
            # Увеличиваем счетчик лодок
            boats += 1

        # Выводим минимальное количество лодок
        print(boats)
